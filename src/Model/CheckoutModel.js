// CheckoutModel.js

// Defining a model (object structure) for storing checkout information
const CheckoutModel = {
  name: '',         // Customer's name (empty string as a placeholder)
  email: '',        // Customer's email address (empty string as a placeholder)
  address: '',      // Shipping address for the order (empty string as a placeholder)
  cardNumber: '',   // Credit or debit card number (empty string as a placeholder)
  expiryDate: '',   // Expiry date of the card (empty string as a placeholder)
  cvv: '',          // CVV (Card Verification Value) of the card (empty string as a placeholder)
};

// Exporting the CheckoutModel object so it can be used in other parts of the application
export default CheckoutModel;
