// ProductModel.js

// Defining a model (object structure) for storing product information
const ProductModel = {
  id: null,         // Unique identifier for the product (initialized as null)
  name: '',         // Name of the product (empty string as a placeholder)
  description: '',  // Description of the product (empty string as a placeholder)
  imgSrc: '',       // Image source (URL or path) for the product image (empty string as a placeholder)
  price: ''         // Price of the product (empty string as a placeholder)
};

// Exporting the ProductModel object so it can be used in other parts of the application
export default ProductModel;
